package seller;

public class FormVO {
	private int form_num;
	private String name;
	private String category;
	private String tel;
	private String brand_site;
	private String email;
	private String seller_id_fk;
	
	public int getForm_num() {
		return form_num;
	}
	public void setForm_num(int form_num) {
		this.form_num = form_num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getBrand_site() {
		return brand_site;
	}
	public void setBrand_site(String brand_site) {
		this.brand_site = brand_site;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSeller_id_fk() {
		return seller_id_fk;
	}
	public void setSeller_id_fk(String seller_id_fk) {
		this.seller_id_fk = seller_id_fk;
	}
	
	
	
}
